<H1>Video Games</H1>


<A HREF="http://onehouronelife.com">One Hour One Life</A>-- my twelfth game.  A multiplayer survival game of parenting and civilization building.  Available now.<BR><BR>

<A HREF="http://cordialminuet.com">Cordial Minuet</A>-- my eleventh game.  A two-player online strategy game played for real money.<BR><BR>

<A HREF="http://thecastledoctrine.net">The Castle Doctrine</A>-- my tenth game.  A massively-multiplayer game of burglary and home defense.<BR><BR>

<A HREF="http://diamondtrustgame.com">Diamond Trust of London</A>-- my ninth game.  A two-player strategy game about deception.<BR><BR>

<A HREF="http://insideastarfilledsky.net">Inside a Star-filled Sky</A>-- my eighth game.  An infinite, recursive, tactical shooter for one player.<BR><BR>

<A HREF="http://sleepisdeath.net">Sleep Is Death</A>-- my seventh game.  A two-player storytelling game.<BR><BR>


<A HREF="http://primrose.sourceforge.net">Primrose</A>-- my sixth game.  A compelling tile-placement puzzle game.  Designed with the iPhone in mind.<BR><BR>

<A HREF="http://www.esquire.com/news-politics/a5329/rohrer-game/">Between</A>-- my fifth game, about consciousness and isolation.  Hosted by <i>Esquire</i> magazine as part of their December 2008 Best and Brightest feature (see <a href="http://www.esquire.com/features/best-and-brightest-2008/future-of-video-game-design-1208">my profile</a> in <i>Esquire</i>).<BR><BR>


<A HREF="http://hcsoftware.sourceforge.net/gravitation">Gravitation</A>-- my fourth game, about mania, melancholia, and the creative process.<BR><BR>

<A HREF="http://hcsoftware.sourceforge.net/passage">Passage</A>-- my third game, shown at Kokoromi's curated <a href="http://www.kokoromi.org/projects/gamma256">GAMMA 256</a> event (at <a href="http://www.sijm.ca/en">MIGS</a> 2007).<BR><BR>

<A HREF="http://cultivation.sourceforge.net">Cultivation</A>-- my second game, a social simulation about a community of gardeners that explores the balance between conflict and compromise (no shooting, but plenty of angry looks).<BR><BR>

<A HREF="http://transcend.sourceforge.net">Transcend</A>-- my first game, an abstract 2D shooting game that doubles as a  multimedia sculpture.<BR><BR>

<A HREF="http://northcountrynotes.org/jason-rohrer/arthouseGames">Arthouse Games</A>-- my artgame journalism site, featuring news, reviews, and interviews.<BR><BR>


